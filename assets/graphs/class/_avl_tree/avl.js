
// AVL Tree

// The AVL tree is a self-balancing binary search tree.
// In an AVL tree, the heights of the two child subtrees of any node differ by at most one.

// Lookup, insertion, and deletion all take O(log n) time in both the average and worst cases.

// AVL trees are often compared with red–black trees. AVL trees are more rigidly balanced than
// red–black trees, leading to faster retrieval but slower insertion and deletion.

// Therefore, for 'lookup-intensive' applications, AVL trees are better.
// For 'real-time' applications, red-black trees are better.

